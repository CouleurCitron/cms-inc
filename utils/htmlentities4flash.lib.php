function htmlSwfFriendly($sHtml){
	$aEvil = array('<strong>', '</strong>');
	$aGood = array('<b>', '</b>');
	return str_replace($aEvil, $aGood, $sHtml);
}

function flashurl_encode($str){
	$search = array("%", "'", "\"", "+", "?", "&");
	$replace = array("%25", "%27", "%22", "%2B", "%3F", "%26");

	return str_replace($search, $replace, $str);
}

function flashurl_decode($str){
	$search = array("%25", "%27", "%22", "%2B", "%3F", "%26");
	$replace = array("%", "'", "\"", "+", "?", "&");

	return str_replace($search, $replace, $str);
}

